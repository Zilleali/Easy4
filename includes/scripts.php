$.fn.jQuerySimpleCounter = function (options) {
        var settings = $.extend({
          start: 0,
          end: 100,
          easing: 'swing',
          duration: 400,
          complete: ''
        }, options);

        var thisElement = $(this);

        $({ count: settings.start }).animate({ count: settings.end }, {
          duration: settings.duration,
          easing: settings.easing,
          step: function () {
            var mathCount = Math.ceil(this.count);
            thisElement.text(mathCount);
          },
          complete: settings.complete
        });
      };
	  	// Time Set for counter complete
      $('#number1').jQuerySimpleCounter({ end: 12, duration: 9000 });
      $('#number2').jQuerySimpleCounter({ end: 100, duration: 5000 });
      $('#number3').jQuerySimpleCounter({ end: 359, duration: 6000 });
      $('#number4').jQuerySimpleCounter({ end: 246, duration: 5500 });
</script>
